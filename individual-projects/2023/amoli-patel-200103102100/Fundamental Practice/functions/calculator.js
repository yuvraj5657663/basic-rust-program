// Calculator - add sub mul div function

function add(a, b) {
    console.log('Addition:', a + b)
}
function sub(a, b) {
    console.log('Subtraction:', a - b)
}
function mul(a, b) {
    console.log('Multiplication:', a * b)
}
function div(a, b) {
    console.log('Division:', a / b)
}
function main() {
    let a = 2
    let b = 5
    add(a, b)
    sub(a, b)
    mul(a, b)
    div(a, b)
}
main()
