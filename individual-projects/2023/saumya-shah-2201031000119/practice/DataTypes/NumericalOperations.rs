fn main(){
    //Addition
    let sum = 10 + 15;
    
    //Subtraction
    let diff = 15 - 10;
    
    //Multiplication
    let product = 10 * 15;
    
    //Division
    let div = 15/10; //results in (1)
    let div2 = -5/3; //results in (-1)
    
    //Reminder
    let rem = 15 % 10;
    
    println!("Addition : {sum}");
    println!("Subtraction : {diff}");
    println!("Multiplication : {product}");
    println!("Division : {div}");
    println!("Division : {div2}");
    println!("Reminder : {rem}");
}