const operator = '+';

const number1 = 15;
const number2 = 12;

let result;

if (operator == '+'){
    result = number1 + number2;
}else if(operator == '-'){
    result = number1 - number2;
}else if(operator == '*'){
    result = number1 * number2;
}else if(operator == '/'){
    result = number1 / number2;
}

